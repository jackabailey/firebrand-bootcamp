# Typecasting is where one variables data is 'casted' onto another data type.
# This allows, for example, the conversion of integers to strings

number = 54
numberAsString = str(number)
# Here, the variable of number has been casted to a string.

print(type(number))
print(type(numberAsString))
# By running the program here you can see that the type has been changed.


# Python will usually dynamically select the type of variable required for the data you have provided.
